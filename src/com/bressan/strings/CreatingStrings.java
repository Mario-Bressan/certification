package com.bressan.strings;

public class CreatingStrings {
    public static void main(String[] args) {

        String s = "Mario"; // one String "Mario"
        s.concat("Bressan"); // two Strings "Bressan" and "MarioBressan"
        s.toLowerCase();    // new String "mario"
        System.out.println(s); // prints Mario

        //How many String objects are created?

    }
}
